const marketSearchUrl = (source: string, town: string, county: string, price: number) => {
  const domain =
    source === "Rightmove"
      ? "rightmove.co.uk"
      : source === "Zoopla"
        ? "zoopla.co.uk"
        : "onthemarket.com";

  const query = encodeURIComponent(
    `site:${domain} 3 bed house garden ${town} ${county} max price ${price}`
  );

  return `https://www.google.com/search?q=${query}`;
};

